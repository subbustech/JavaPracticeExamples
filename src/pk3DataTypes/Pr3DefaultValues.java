package pk3DataTypes;

public class Pr3DefaultValues {
	byte b;
	short s;
	int i;
	long l;
	float f;
	double d;
	boolean bo;
	char ch;
	public static void main(String[] args) {
		
		Pr3DefaultValues pr3 = new Pr3DefaultValues();

		System.out.println("byte default value is "+pr3.b);
		System.out.println("short default value is "+pr3.s);
		System.out.println("int default value is "+pr3.i);
		System.out.println("long default value is "+pr3.l);
		System.out.println("float default value is "+pr3.f);
		System.out.println("double default value is "+pr3.d);
		System.out.println("boolean default value is "+pr3.bo);
		System.out.println("char default value is "+pr3.ch);



	}

}
